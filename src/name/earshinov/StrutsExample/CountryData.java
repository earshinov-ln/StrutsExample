package name.earshinov.StrutsExample;

public class CountryData {

	String id;
	String name;
	
	public CountryData(String id, String name) {
		this.id = id;
		this.name = name;
	}
	
	
	// геттеры, сеттеры
	
	public String getId() {
		return id;
	}

	public String getName() {
		return name;
	}	
}
